import numpy as np

class Sigmoid:
    def __init__(self):
        self.params = []

    def forward(self, x):
        return 1 / (1 + np.exp(-x))


class Affine:
    def __init__(self, W, b):
        self.params = [W, b]

    def forward(self, x):
        W, b = self.params
        out = np.matmul(x, W) + b
        return out
    
class TwoLayerNet:
    def __init__(self, input_size, hidden_size, output_size):
        input = input_size
        hidden = hidden_size
        output = output_size

        # 가중치, 편향 초기화
        W1 = np.random.randn(input, hidden)   # randn -> 정규 분포 형태의 랜덤값이 있는 행렬을 생성한다.
        b1 = np.random.randn(hidden)
        W2 = np.random.randn(hidden, output)
        b2 = np.random.randn(output)

        # 계층 생성
        self.layers = [Affine(W1, b1), Sigmoid(), Affine(W2, b2)]

        # 모든 가중치를 리스트에 모은다.
        self.params = []
        for layer in self.layers:
            self.params += layer.params

    def predict(self, x):
        for layer in self.layers:
            x = layer.forward(x)
        return x

